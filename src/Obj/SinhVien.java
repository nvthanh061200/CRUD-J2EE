package Obj;

public class SinhVien {
	public String id, name;

	public SinhVien(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public SinhVien() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
